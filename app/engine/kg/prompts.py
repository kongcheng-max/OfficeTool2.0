# AI 算法调优目标:
#   - 关系类型: BELONGS_TO(属于), SIGNS(签署), OWNS(拥有), PART_OF(组成部分),
#              RELATED_TO(相关), RESPONSIBLE_FOR(负责), HAPPENED_AT(发生于),
#              INVOLVES(涉及), CONTRADICTS(矛盾/冲突), SUPERSEDES(替代/更新)
#   - 基于已知实体列表推断关系, 不凭空编造
#   - 输出包含 subject/object 均为已知实体名
#   - 同一对实体可能有多种关系, 每种关系单独列出
